let cars=["BMW","Audi","Mercedes"];
console.log(cars.slice()); // [ 'BMW', 'Audi', 'Mercedes' ]   // Slice function is called without any arguments

console.log(cars.slice(1)); // [ 'Audi', 'Mercedes' ]   // Slice function is called with one argument

console.log(cars.slice(2)); // [ 'Audi' ]   // Slice function is called with two arguments